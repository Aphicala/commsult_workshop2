var number = 10

if (number > 10) {
  console.log('The number is bigger than 10')
} else if (number === 10) {
  console.log('The number is 10')
} else {
  console.log('The number is smaller than 10')
}

var month = 2

switch (month) {
  case 1:
    console.log('January')
    break

  case 2:
    console.log('February')
    break

  case 3:
    console.log('March')
    break

  case 4:
    console.log('April')
    break

  case 5:
    console.log('May')
    break

  default:
    console.log('Not a month')
}
